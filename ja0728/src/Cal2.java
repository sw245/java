
public class Cal2 {
	
	int add(int a, int b, int c) {
		int result = a + b + c;
		return result;
	}
	
	int subtract(int a, int b, int c) {
		int result = a - b - c;
		return result;
	}
	
	int multiply(int a, int b, int c) {
		int result = a * b * c;
		return result;
	}
	
	int divide(int a, int b, int c) {
		int result = a / b / c;
		return result;
	}
	
}
